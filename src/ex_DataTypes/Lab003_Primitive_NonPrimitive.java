package ex_DataTypes;

public class Lab003_Primitive_NonPrimitive {
    public static void main(String[] args){
        // data_type variable_name = variable_value(literal)

        // Data Type
        // Primitive Data Type
        //  boolean
        // true or false
        boolean is_married = true;
        boolean are_you_a_God = false;
        System.out.println(is_married);
        System.out.println(are_you_a_God);

        // Numerical
        // integral - byte, short, int, long
        byte b  = 100;
        short s = 101;
        int age = 130;
        long phone_number = 9876543210L; // 9876543210l;

        //  char - char  - A,B
        char grade  = 'A'; // O,A,B,C,D,E, F = Fail

        // floating
        // float
        float pi = 3.14f; // 3.14F;
        // double
        double d = 118.654;


        // Non primitive Data Types
        // String  name ->"snehal" -, number or char or bunch of char
        String name = "Snehal";
        String name2 = "s";
//        char cc = "c";
    }
}
