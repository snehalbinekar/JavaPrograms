package ex_TypeCasting;

public class Lab031 {
    public static void main(String [] args){
        String s1 = "Snehal";
        String s2 = s1;
        System.out.println(s2);// Snehal
        String s3 = s2.toLowerCase();
        System.out.println(s3); //snehal
        System.out.println(s1); //Snehal
    }
}
