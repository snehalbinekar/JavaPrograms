package ex_Operators;

public class Lab016 {
    public static void main(String[] args){
        //Relational Operator
        System.out.println(10 == 10);
        System.out.println( 10 >= 10); //  10> 10 or 10 = 10
        System.out.println( 10 <= 10); // 10 < 10 or 10 = 10
        System.out.println( 10 > 10);
        System.out.println( 10 < 10);
    }
}
