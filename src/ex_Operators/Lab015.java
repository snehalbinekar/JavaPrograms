package ex_Operators;

public class Lab015 {
    public static void main(String[] args){
        //Compound Assignment Operator
        int a = 10;
        // += , -=, /=, *= . %=
        // += ->
        a+=10; // a = a+10
        a-=10; // a = a-10
        a/=10; // a = a/10
        a*=10; // a = a*10
    }
}
